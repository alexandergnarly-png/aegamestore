function isToolsGame(gameName) {
  const name = String(gameName || "").toLowerCase();

  return (
    name.includes("gbox") ||
    name.includes("g box") ||
    name.includes("tools") ||
    name.includes("tool")
  );
}

function getVisibleGames() {
  const uniqueGames = [...new Set(allProducts.map((item) => item.game))];

  if (currentCategory === "Tools") {
    return uniqueGames.filter((game) => isToolsGame(game));
  }

  if (currentCategory === "Mobile") {
    return uniqueGames.filter((game) => !isToolsGame(game));
  }

  return uniqueGames;
}

function renderGames() {
  gameGrid.innerHTML = "";

  const visibleGames = getVisibleGames();

  if (visibleGames.length === 0) {
    const emptyTitle =
      currentLanguage === "en"
        ? "No products in this category yet"
        : "Belum ada produk di kategori ini";

    const emptyDesc =
      currentLanguage === "en"
        ? "Try another category or contact admin for product availability."
        : "Coba kategori lain atau hubungi admin untuk cek ketersediaan produk.";

    const emptyBtn = currentLanguage === "en" ? "Chat Admin" : "Chat Admin";

    gameGrid.innerHTML = `
    <div class="empty-category-card">
      <div class="empty-category-icon">🌊</div>
      <h3>${emptyTitle}</h3>
      <p>${emptyDesc}</p>
      <a href="https://t.me/aegamestore" target="_blank">${emptyBtn}</a>
    </div>
  `;
    return;
  }

  visibleGames.forEach((game) => {
    const card = document.createElement("div");
    card.className = "game-card";

    const imageUrl = gameImages[game] || fallbackImage;
    const gameProducts = allProducts.filter((item) => item.game === game);
    const availableProducts = gameProducts.filter(
      (item) => Number(item.available_keys || 0) > 0,
    );
    const totalStock = gameProducts.reduce(
      (total, item) => total + Number(item.available_keys || 0),
      0,
    );
    const minPrice =
      availableProducts.length > 0
        ? Math.min(...availableProducts.map((item) => Number(item.price || 0)))
        : 0;

    const stockLabel =
      totalStock > 0
        ? currentLanguage === "en"
          ? `${totalStock} stock ready`
          : `${totalStock} stok ready`
        : translations[currentLanguage].outOfStockLabel;

    const priceLabel =
      minPrice > 0
        ? currentLanguage === "en"
          ? `From ${formatRupiah(minPrice)}`
          : `Mulai ${formatRupiah(minPrice)}`
        : currentLanguage === "en"
          ? "Not available"
          : "Belum tersedia";

    card.innerHTML = `
  <img src="${imageUrl}" alt="${game}" onerror="this.src='${fallbackImage}'">
  <span>${game}</span>
  <div class="game-card-meta">
    <small>${priceLabel}</small>
    <b class="${totalStock > 0 ? "ready" : "empty"}">${stockLabel}</b>
  </div>
`;

    if (game === selectedGame) {
      card.classList.add("active");
    }

    card.onclick = async () => {
      card.style.pointerEvents = "none";

      try {
        await openOrderModal(game);
      } finally {
        card.style.pointerEvents = "auto";
      }
    };

    gameGrid.appendChild(card);
  });
}

async function openOrderModal(game) {
  const voucherInput = document.getElementById("voucherCodeInput");
  if (voucherInput) voucherInput.value = "";

  const nameInput = document.getElementById("name");
  const contactInput = document.getElementById("contact");

  resetVoucherPreview();

  try {
    const res = await fetch("/api/user/me");
    const data = await res.json();

    if (!data.loggedIn) {
      Swal.fire({
        icon: "warning",
        title: "Login Dulu",
        text: "Kamu harus login dulu sebelum order.",
        confirmButtonColor: "#0ea5e9",
        confirmButtonText: "Login Sekarang",
      }).then(() => {
        window.location.href = "/auth";
      });
      return;
    }

    if (nameInput && !nameInput.value.trim()) {
      nameInput.value = data.username || "";
    }

    if (contactInput && data.contact && !contactInput.value.trim()) {
      contactInput.value = data.contact;
    }
  } catch (err) {
    Swal.fire({
      icon: "error",
      title: "Gagal Cek Login",
      text: "Coba refresh halaman lalu login ulang.",
      confirmButtonColor: "#fb7185",
    });
    return;
  }

  selectedGame = game;
  renderGames();
  loadBrands();

  const modal = document.getElementById("orderModal");
  const title = document.getElementById("modalGameTitle");

  if (title) {
    title.innerText = game;
  }

  if (modal) {
    modal.classList.add("show");
    document.body.style.overflow = "hidden";
  }
}

function closeOrderModal() {
  const modal = document.getElementById("orderModal");

  if (modal) {
    modal.classList.remove("show");
    document.body.style.overflow = "";
  }

  const voucherInput = document.getElementById("voucherCodeInput");
  if (voucherInput) voucherInput.value = "";

  resetVoucherPreview();
}

function loadBrands() {
  const brands = [
    ...new Set(
      allProducts
        .filter((item) => item.game === selectedGame)
        .map((item) => item.brand),
    ),
  ];

  brandSelect.innerHTML = "";

  brands.forEach((brand) => {
    const option = document.createElement("option");
    option.value = brand;
    option.textContent = brand;
    brandSelect.appendChild(option);
  });

  loadDurations();
}

function loadDurations() {
  const filteredProducts = allProducts.filter(
    (item) => item.game === selectedGame && item.brand === brandSelect.value,
  );

  productSelect.innerHTML = "";

  filteredProducts.forEach((item) => {
    const availableKeys = Number(item.available_keys || 0);
    const isOutOfStock = availableKeys <= 0;

    const stockText = isOutOfStock
      ? translations[currentLanguage].outOfStockLabel
      : `${translations[currentLanguage].stockLabel} ${availableKeys}`;

    const option = document.createElement("option");
    option.value = item.id;
    option.disabled = isOutOfStock;
    option.textContent = `${item.duration} - ${formatRupiah(item.price)} - ${stockText}`;
    productSelect.appendChild(option);
  });

  const firstAvailable = filteredProducts.find(
    (item) => Number(item.available_keys || 0) > 0,
  );

  if (firstAvailable) {
    productSelect.value = firstAvailable.id;
  } else if (filteredProducts[0]) {
    productSelect.value = filteredProducts[0].id;
  }

  updatePreview();
}

function updatePreview() {
  const selectedProduct = allProducts.find(
    (item) => String(item.id) === String(productSelect.value),
  );

  if (!selectedProduct) {
    selectedProductId = null;
    selectedProductBasePrice = 0;

    document.getElementById("previewGame").innerText =
      translations[currentLanguage].previewEmpty;
    document.getElementById("previewProduct").innerText =
      translations[currentLanguage].previewWait;
    document.getElementById("previewPrice").innerText = "Rp 0";
    resetVoucherPreview();
    return;
  }

  selectedProductId = selectedProduct.id;
  selectedProductBasePrice = Number(selectedProduct.price || 0);

  const availableKeys = Number(selectedProduct.available_keys || 0);
  const isOutOfStock = availableKeys <= 0;

  document.getElementById("previewGame").innerText = selectedProduct.game;
  document.getElementById("previewProduct").innerText =
    `${selectedProduct.brand} - ${selectedProduct.duration}`;
  showDefaultPriceBreakdown(selectedProduct.price);

  buyBtn.disabled = isOutOfStock;
  buyBtn.innerText = isOutOfStock
    ? translations[currentLanguage].outOfStockLabel
    : translations[currentLanguage].buyNow;

  resetVoucherPreview();
}

brandSelect.addEventListener("change", loadDurations);
productSelect.addEventListener("change", updatePreview);

async function buy() {
  const name = document.getElementById("name").value.trim();
  const contact = document.getElementById("contact").value.trim();

  const selectedProduct = allProducts.find(
    (item) => String(item.id) === String(productSelect.value),
  );

  if (!name || !contact) {
    Swal.fire({
      icon: "warning",
      title: "Oops...",
      text: "Isi nama player dan kontak kamu dulu ya!",
      confirmButtonColor: "#0ea5e9",
    });
    return;
  }
  if (!selectedProduct) {
    Swal.fire({
      icon: "info",
      title: "Pilih Produk",
      text: "Pilih game dan durasi produknya dulu.",
      confirmButtonColor: "#0ea5e9",
    });
    return;
  }

  const availableKeys = Number(selectedProduct.available_keys || 0);

  if (availableKeys <= 0) {
    closeOrderModal();
    loadAllProducts();

    Swal.fire({
      icon: "error",
      title: translations[currentLanguage].outOfStockTitle,
      text: translations[currentLanguage].outOfStockText,
      confirmButtonColor: "#fb7185",
    });

    return;
  }

  setLoading(true);

  try {
    const res = await fetch("/create-order", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        product_id: selectedProduct.id,
        name,
        contact,
        voucher_code:
          appliedVoucherCode ||
          document.getElementById("voucherCodeInput")?.value ||
          "",
      }),
    });

    const data = await res.json();
    if (res.status === 401) {
      Swal.fire({
        icon: "warning",
        title: "Login Dulu",
        text: data.message || "Kamu harus login dulu sebelum order",
        confirmButtonColor: "#0ea5e9",
      }).then(() => {
        window.location.href = data.redirectUrl || "/auth";
      });
      return;
    }

    if (data.paymentUrl) {
      window.location.href = data.paymentUrl;
      return;
    }

    if (data.resultUrl) {
      window.location.href = data.resultUrl;
      return;
    }

    const errorMessage = data.message || "Gagal membuat pembayaran";
    const isOutOfStock = errorMessage.toLowerCase().includes("stok key habis");

    if (isOutOfStock) {
      closeOrderModal();
      loadAllProducts();

      Swal.fire({
        icon: "error",
        title: translations[currentLanguage].outOfStockTitle,
        text: errorMessage,
        confirmButtonColor: "#fb7185",
      });

      return;
    }

    Swal.fire({
      icon: "error",
      title: "Gagal",
      text: errorMessage,
      confirmButtonColor: "#fb7185",
    });
  } catch (err) {
    Swal.fire({
      icon: "error",
      title: "Error",
      text: "Terjadi error server",
      confirmButtonColor: "#fb7185",
    });
  }

  setLoading(false);
}
